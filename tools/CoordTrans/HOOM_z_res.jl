function getHOOMResolution(
    res
)

    local zs

    res = Symbol(res)

    if res == :POP2

        zs = getPOP2VerticalResolution()[1:41]

    elseif res == :Standard

        zs = collect(Float64, 0:-25:-500)[1:end-1]
        zs = vcat(zs, collect(Float64, -500:-50:-1000))
        if length(zs) != 31
            println(zs)
            throw(ErrorException("Not expected zs. Please check."))
        end

    else

        throw(ErrorException("Unknown resolution `" * string(res) * "`"))

    end

    return zs

end

